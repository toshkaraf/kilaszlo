class Topic {
  final String id;
  final String name;
  final String? description;
  final List<Topic> subtopics;

  Topic({
    required this.id,
    required this.name,
    this.description,
    this.subtopics = const [],
  });

  String getName(bool isGerman) {
    if (!isGerman) return name;
    return _topicTranslations[id]?['name'] ?? name;
  }

  String? getDescription(bool isGerman) {
    if (!isGerman) return description;
    return _topicTranslations[id]?['description'] ?? description;
  }
}

class ThemeCategory {
  final String id;
  final String name;
  final String description;
  final List<ThemeSubcategory> subcategories;

  ThemeCategory({
    required this.id,
    required this.name,
    required this.description,
    required this.subcategories,
  });

  String getName(bool isGerman) {
    if (!isGerman) return name;
